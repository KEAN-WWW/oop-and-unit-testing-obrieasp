class Calculator(object):
    @staticmethod
    def addition(val1, val2):
        return val1 + val2

    @staticmethod
    def subtraction(self, val1, val2):
        return val1 - val2

    @staticmethod
    def multiply(self, val1, val2):
        return val1 * val2

    @staticmethod
    def divide(self, val1, val2):
        return val1 / val2